package CursinhoCoder.Desafios.Fundamentos;

public class DesafioAtitmeticos {

	public static void main(String[] args) {

		
		double a , b, c, d,  e, f, g, h, i, j, k, l, m, n, o, p;
		
		
		a = 6;
		b = (3 + 2);
		c = a * b;
		
		d = Math.pow(c, 2);
		
		e = (3 * 2);
		
		f = d / e;
				
		g = (1 - 5);
		
		h = (2 - 7);
		
		i = g * h;
		
		j = i / 2;
		
		k = Math.pow(j, 2);
		
		l = f - k;
		
		m = Math.pow(l, 3);
		
		n = 10;
		
		o = Math.pow(n, 3);
		
		p = m / o;
		
		System.out.println("Resultado: " + p);

	}

}
